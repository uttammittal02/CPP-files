bool match(string &s, string &t, int i){
    int j = 0;
    for (; i < s.size(); i++){
        if (s[i] != t[j]) return false;
        j ++;
        if (j == t.size()) return true;
    }
    return j == t.size();
}

int32_t main()
{
	string s; cin >> s;
    string a = "gggtcgcgtt", b = "aaccatgccg", c = "gaagagtctt";
    int cnt = 0;
    for(int i = 0; i < s.size(); i ++){
        cnt += (match(s, a, i)? 1 : 0);
    }
    cout << cnt << endl;
    cnt = 0;
    for(int i = 0; i < s.size(); i ++){
        cnt += (match(s, b, i)? 1 : 0);
    }
    cout << cnt << endl;
    cnt = 0;
    for(int i = 0; i < s.size(); i ++){
        cnt += (match(s, c, i)? 1 : 0);
    }
    cout << cnt << endl;
	return 0;
}

