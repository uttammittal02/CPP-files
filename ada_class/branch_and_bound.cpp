int upper = infinity;
vector <vi > adj(4, vi(4, infinity));

void dfs(vi vis, int cost, int idx, int tot){
    if (cost > upper) return;
    if (accumulate(all(vis), 0) == sz(vis)) {
        upper = min(upper, cost);
        return;
    }
    for (int i = 0; i < sz(vis); i++) {
        if (vis[i] == 0) {
            vis[i] = 1;
            dfs(vis, cost + adj[idx][i], i, tot);
            vis[i] = 0;
        }
    }
}

