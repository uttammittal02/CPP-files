#include <bits/stdc++.h>
using namespace std;
// ----------------------------------------------------------------------------------------------------------- //
// ----------------------------------------------------------------------------------------------------------- //
int32_t main()
{
    int n; cin >> n;
    vector <int> arr(n);
    

    for(int i =0; i < n; i++)
        cin >> arr[i];
    for(int i = 0; i < n - 1; i ++)
    {
        bool done = true;
        int idx_min_element = min_element(arr.begin() + i, arr.end()) - arr.begin();
        // swap(arr[i], arr[idx_min_element]);
        for (int j = idx_min_element; j > i; j--)
            swap(arr[j], arr[j - 1]);
        
        for(int i =0; i < n; i ++)
        {
            if (i and arr[i] < arr[i - 1])
                done = false;

            cout << arr[i] << ' ';
        }
        if (done)
            break;
        cout << endl;
    }
    if (n == 1)
        cout << arr[0];
    
    return 0;
}
